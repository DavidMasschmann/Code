package Activity05;

public class Activity05_2 {
    /*
    Since all strings are String objects, show how to call the
    length () and charAt () methods in this string literal: “I like Java”.
    */
    public static void main(String[] args) {
        String test = "I like java";

        System.out.println(test.length());
        System.out.println(test.charAt(9));
    }
}